<div class="seccion_1">
<h1 class="contenedor_0">SOLICITUD DE EVALUACION INTEGRAL</h1>

<p class="columns">El  trámite de Solicitud para la Evaluación Integral de Productos Naturales, es un paso previo al trámite de Registro Sanitario de Productos Naturales ante al Servicio Autónomo de Contraloría Sanitaria (SACS) del Ministerio del Poder Popular para la Salud (MPPS), en conformidad con la Resolución publicada en Gaceta Oficial Número 38.590 de fecha 22 de Diciembre de 2006 y la Resolución SG-1329, publicada en Gaceta Oficial Nº 35.837 de fecha 14 de Noviembre de 1995.</p>
<p class="columns">Pasos a seguir para la solicitud de evaluacion integral de productos naturales:</p>
<ol class="numero">
<li class="numero">Descargue el Formulario <a class="resaltado" href="doc/rc_doc/rc_pn/F-DMPN-001 SEIPN.rtf" title="F-DMPN-001 “SOLICITUD PARA LA EVALUACION INTEGRAL DE PRODUCTOS NATURALES”" target="_blank">F-DMPN-001 “SOLICITUD PARA LA EVALUACION INTEGRAL DE PRODUCTOS NATURALES”. &raquo; </a></li>
<li class="numero">Complete el formulario y prepare los anexos respectivos, según se indica en el <a class="resaltado" href="pdf/rc_pdf/rc_pn/Instructivo F-DMPN-001 SEIPN.pdf" title="F-DMPN-001 “SOLICITUD PARA LA EVALUACION INTEGRAL DE PRODUCTOS NATURALES”" target="_blank">Instructivo del Formulario F-DMPN-001 &raquo; </a> “SOLICITUD PARA LA EVALUACION INTEGRAL DE PRODUCTOS NATURALES”.</li>
<li class="numero">Estampe dos (2) timbres fiscales de 0,01 Unidad Tributaria para cada página del Formulario F-DMPN-001 o su equivalente.</li>
<li class="numero">Descargue el formato de <a class="resaltado" href="doc/rc_doc/rc_pn/Etiquetas Carpetas PN.doc" title="ETIQUETAS PARA LA IDENTIFICACION DE LA CARPETA" target="_blank">ETIQUETAS PARA LA IDENTIFICACION DE LA CARPETA. &raquo; </a></li>
<li class="numero">Prepare una carpeta especial tamaño oficio con la etiqueta descargada en el paso 5 en la portada y las pestañas con el “NOMBRE DEL PRODUCTO NATURAL - EXPEDIENTE FÍSICO", y archive lo siguiente:
	<ul class="vinetas">
    <li class="vinetas">Formulario F-DMPN-001 “Solicitud para la Evaluación Integral de Productos Naturales” en original con sus respectivas firmas, sellos y timbres fiscales.</li>
    <li class="vinetas">Anexos según el Instructivo del Formulario F-DMPN-001.</li>
    <li class="vinetas">Anexe el Original y 1 Fotocopia de la Planilla del Depósito Bancario donde realizó el pago correspondiente según la tarifa vigente y lo establecido en la Circular <a class="resaltado" href="pdf/rc_pdf/P-178-2012-10-02-2012.pdf" title="P- Nº P-178/2012 DE FECHA 10/02/2012 - Normativa sobre Pago de Servicios." target="_blank">P- Nº P-178/2012 DE FECHA 10/02/2012 - Normativa sobre Pago de Servicios. &raquo; </a></li>
    </ul>
</li>
<li class="numero">Guarde el Formulario F-DMPN-001 y todos los Anexos del Expediente Físico en formato digital, según INSTRUCTIVO <a class="resaltado" href="pdf/rc_pdf/rc_pn/instructivo_estructura_cd-rom_pn.pdf" title="ESTRUCTURA DE CD-ROM PRODUCTOS c NATURALES" target="_blank">“ESTRUCTURA DE CD-ROM PRODUCTOS c NATURALES. &raquo; </a></li>
<li class="numero">Prepare la cantidad de Muestras y Patrones según el Cuadro 1 "Cantidad de Muestras de Producto Naturales", contenido en el Instructivo del Formulario F-DMPN-001.</li>
<li class="numero">Solicite  una Cita para la entrega de solicitudes de Productos Naturales , según lo establecido en el vinculo <a class="resaltado" href="http://190.202.114.146:7474/modulos/" title="Sistema de Citas para Entrega de Solicitudes" target="_blank">"Sistema de Citas para Entrega de Solicitudes" &raquo; </a></li>
<li class="numero">Consigne el día de la cita pautada ante Departamento de Productos Naturales lo siguiente:
	<ul class="vinetas">
    <li class="vinetas">Expediente Físico.</li>
    <li class="vinetas">Cinco (5) Discos Compactos (CD).</li>
    <li class="vinetas">Muestras y Patrones.</li>
	</ul> 
</li>
<li class="numero">El funcionario receptor verificará que todos los requisitos solicitados  estén presentes en la solicitud entregada y procederá a llenar el  formulario F-DMPN-002 Recepción de la Solicitud para la Evaluación Integral de Productos Naturales. El Patrocinante recibirá una copia como constancia de entrega de la solicitud respectiva.</li>
<li class="numero">En la caja principal del edificio sede, el Patrocinante deberá tramitar la factura respectiva al pago del servicio solicitado</li>
<li class="numero">Admision:</strong> Una vez recibida la solicitud de evaluacion integral, se revisara que cumpla con los requisitos establecidos para su admision y evaluacion integra. La emisión de los resultados de la verificación y Admisión se efectuara en un lapso de 20 días hábiles posterior a la recepción del producto.
	<ul class="vinetas">
    <li class="vinetas">Nota:</strong>En caso que la solicitud no sea admitida, deberá ser retirada por  la Unidad  de Recepción respectiva y una vez completados los recaudos, la solicitud podrá ser nuevamente presentada ante la Unidad de recepción, una vez solicitada la cita respectiva, en el lapso del periodo de vigencia del deposito, según lo establecido en la Circular “Normativa para la Cancelación de Pagos” Circular <a class="resaltado"  href="pdf/rc_pdf/P-178-2012-10-02-2012.pdf" title="Nº P-178/2012 DE FECHA 10/02/2012." target="_blank">Nº P-178/2012 DE FECHA 10/02/2012. &raquo; </a></li>
    </ul>
</li>
<h3 class="contenedor_1">EMISIÓN DE RESULTADOS</h3>
<br />
<li class="numero">Evaluacion Integral:</strong> Una vez admitida la solicitud, se inicia el proceso de evaluación y análisis del producto.</li>
<li class="numero">Emisión de Solicitudes de Recaudos:</strong> En caso de requerir información adicional para concluir la evaluación, será solicitada mediante  oficio  dirigido al Farmacéutico Patrocinante y a la Casa de representación del producto.</li>
<li class="numero">Emisión de Resultado de la Evaluación Integral de Productos Naturales:</strong> Los resultados de la evaluación integral seran remitidos al Servicio Autónomo de Contraloría Sanitaria y se emitirá  un oficio dirigido al Farmacéutico Patrocinante y a la Casa de representación del producto notificando la  culminación de la Evaluación.</li>
</ol>
