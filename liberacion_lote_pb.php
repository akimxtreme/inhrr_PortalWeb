<h1 class="contenedor_0">LIBERACIÓN LOTE A LOTE</h1>

<p class="columns">Una vez otorgado el registro sanitario a productos biológicos tipo vacunas y Hemoderivados  de origen humano,  el Representante del producto en el país (titular del registro sanitario), deberá solicitar  al Instituto Nacional de Higiene “Rafael Rangel”, a través de su Farmacéutico Patrocinante,  la liberación de cada lote a comercializar, según lo establecido en la  NORMA DE REGISTRO, LIBERACIÓN DE LOTE Y CONTROL DE LOS PRODUCTOS BIOLÓGICOS <a class="resaltado" href="pdf/rc_pdf/N-PERC-001R0.PDF" title="NORMA DE REGISTRO, LIBERACIÓN DE LOTE Y CONTROL DE LOS PRODUCTOS BIOLÓGICOS" target="_blank">Nº  N-PERC-001  de fecha 09/12/2008 &raquo;</a>, empleando el formulario <a class="resaltado" href="doc/rc_doc/rc_pb/F-RCPB-029.doc" title="F-RCPB-029 Solicitud de Liberación de Lote de Productos Biológicos" target="_blank">F-RCPB-029 Solicitud de Liberación de Lote de Productos Biológicos &raquo;</a> según se indica en el <a class="resaltado" href="doc/rc_doc/rc_pb/F-RCPB-029_Instructivo.doc" title="Instructivo F-RCPB-029" target="_blank">Instructivo &raquo;</a> respectivo y cancelar la tarifa respectiva.</p>
<p class="columns3">En caso de ser necesario, para evaluar la calidad y consistencia de producción respectiva, la División de Control Nacional de Productos Biológicos podrá solicitar la entrega de  muestras del lote a liberar para su análisis.</p>
<p class="columns3">El incumplimiento en la solicitud de liberación lote a lote de vacunas y Hemoderivados de origen humano, conlleva a la cancelación del registro sanitario respectivo. </p>
<p class="columns3">En el caso de otros productos biológicos, como Hemoderivados heterologos, afines y biológicos terapéuticos (ADN recombinante, anticuerpos monoclonales, otros), serán sometidos al proceso de liberación de lotes, cuando se requiera hacer seguimiento de su calidad, seguridad y /o eficacia.</p>
