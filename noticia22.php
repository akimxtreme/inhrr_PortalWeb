	<h2 class="contenedor_1">Pueblo Sano implantó primer marcapaso DAI en Yaracuy completamente gratis</h2>
    <div class="sections3">
    	<p class="columns">El Gobierno de Yaracuy de la mano con la Fundación Pueblo Sano, continúa innovando en la colocación de marcapasos para los pacientes, no sólo del estado sino de cualquier parte del país, en función de dignificar al pueblo en materia de salud.</p>
        <p class="columns">Durante el fin de semana fue intervenido quirúrgicamente un paciente del municipio Peña, donde se le implantó por parte de la Fundación Pueblo Sano el primer marcapaso Desfibrilador Automático Implantable (DAI) totalmente gratis.</p>
        <p class="columns">Luis Vázquez, medico intensivista, explicó que el marcapaso DAI es colocado a los pacientes que presentan una patología de extrema dificultad respiratoria, “en este caso el paciente beneficiado se le diagnosticó una enfermedad bronco-pulmonar crónica, que a través de varios estudios realizados, se determinó que éste tenía altísima probabilidad de sufrir una muerte súbita por arritmia de tipo extrasístole ventriculares muy frecuentes, es decir, su vida estaba en riesgo en caso de no realizar la intervención en un periodo corto”.</p>
        <p class="columns">Asimismo señaló, que el marcapaso tiene un dispositivo que le permite detectar las arritmias, través de una descarga eléctrica al momento que el paciente este presentando la dificulta cardiaca que en la mayoría de los casos son letales.</p>
        <p class="columns">El galeno, indicó que gracias a la operación el paciente tendrá la oportunidad de tener una vida más activa, realizando tareas cotidianas que anteriormente producto de la enfermedad lo limitaba debido al cansancio que esta le producía, teniendo una oportunidad para mejorar su calidad de vida.</p>
    </div>
    <div class="sections3">    	
        <img class="columns" src="noticias/img/noticia22.png" alt="Imagen Relacionada con la noticia">
