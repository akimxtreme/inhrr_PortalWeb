        <p class="columns">Eulacio destacó, “este proyecto nace con la misión de beneficiar a los habitantes del estado Yaracuy, sin embargo nuestro trabajo es brindarle la ayuda a todo paciente que la requiera, sin importar de donde venga, es decir, le estamos dando la oportunidad a través del proceso de inclusión a todos los venezolanos”.</p>
