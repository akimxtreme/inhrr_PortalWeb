<h1 class="contenedor_0">ENFERMEDADES TRANSMITIDAS POR ALIMENTOS (ETA)</h1>

<p class="boton_global"><strong>¿QUÉ SON LAS ENFERMEDADES TRANSMITIDAS POR ALIMENTOS (ETA)?</strong></p>
<p class="boton_global">“Síndrome originado por la ingestión de alimentos y/o agua, que contengan agentes etiológicos en cantidades tales que afecten la salud del consumidor a nivel individual o grupos de población.”</p>
<p class="boton_global">“LAS ALERGIAS POR HIPERSENSIBILIDAD INDIVIDUAL A CIERTOS ALIMENTOS NO SE CONSIDERAN ETA”. Fuente: <a href="http://www.msds.gob.ve" title="Fuente" target="_blank"><strong>http://www.msds.gob.ve</strong></a></p>
<p class="boton_global"><strong>¿CUÁL ES LA CLASIFICACIÓN DE LAS ETA?</strong></p>
<p class="boton_global"><strong>Infección Alimentaria:</strong> Resultan de la ingestión de alimentos que contienen la presencia de bacterias u otros microorganismos vivos (como virus, parásitos, hongos) los cuales infectan el cuerpo después del consumo.</p>
<p class="boton_global"><strong>Intoxicación Alimentaria:</strong> Refiere a la ingestión de toxinas que se encuentran en:</p>
<ul class="vinetas">
<li class="vinetas">Tejidos de plantas o animales,</li>
<li class="vinetas">Productos metabólicos de microorganismos en los alimentos,</li>
<li class="vinetas">Sustancias químicas que se incorporan de modo accidental, incidental o intencional en los alimentos.</li>
</ul>
<p class="boton_global">Ocurren cuando las toxinas o venenos (de bacterias o mohos) están presentes en el alimento, incluyendo exotoxinas producidas por el metabolismo de las bacterias. Estas toxinas pueden estar presentes incluso cuando el microorganismo que las produjo ha sido eliminado o esté incapacitado para provocar infección. Fuente: <a href="http://www.panalimentos.org" title="Fuente" target="_blank"><strong>http://www.panalimentos.org</strong></a></p>
<p class="boton_global"><strong>¿QUIÉNES PODEMOS SUFRIR UNA ETA?</strong></p>
<p class="boton_global">Todos somos susceptibles a contraer una enfermedad transmitida por los alimentos. Algunos grupos demográficos están a mayor riesgo que otros como son:</p>
<ul class="vinetas">
<li class="vinetas">Los lactantes y niños pequeños debido a que su sistema inmunológico no está completamente desarrollado como el de los adultos.</li>
<li class="vinetas">Los ancianos, debido a que la efectividad de su sistema inmunológico ha disminuido con la edad.</li>
<li class="vinetas">Individuos enfermos cuyos sistemas inmunológicos no están funcionando apropiadamente por causa de padecimientos como el cáncer, sida u otras enfermedades.</li>
<li class="vinetas">Mujeres embarazadas y sus fetos.</li>
</ul>
<p class="boton_global"><strong>¿CÓMO RECONOCER QUE ESTAMOS EN PRESENCIA DE UNA ETA?</strong></p>
<p class="boton_global">Se pueden evidenciar varios síntomas como: malestar estomacal, diarrea, náusea, vómito, fiebre, escalofríos, dolores abdominales, dolor de cabeza, mareos u otros; si esto ocurre a las pocas horas de haber ingerido alimentos, y además le ocurre a otra persona (que haya ingerido los mismos alimentos), podemos entonces sospechar que estamos en presencia de una ETA. Lo más recomendable es buscar asistencia en un Centro de Salud (Ambulatorio, Hospital o Servicio de Salud Privado).</p>
<p class="boton_global"><strong>¿CUÁL ES EL PROCEDIMIENTO A SEGUIR ANTE UN BROTE DE ETA?</strong></p>
<p class="boton_global">Una vez que se recibe asistencia médica (nivel de asistencia primaria), el médico debe determinar si está en presencia de una ETA, y conjuntamente con el epidemiólogo debe proceder a realizar la denuncia en el Distrito Sanitario al cual pertenece el Centro de Salud; para así iniciar la investigación epidemiológica.</p>
<p class="boton_global">El nivel de atención primaria (Centro de Salud) debe disponer de modelos de encuesta epidemiológica y materiales para toma de muestras; a fin de que el personal, tan pronto detecte los primeros casos, pueda recolectar la información preliminar y proceder a la toma de muestras clínicas hasta que llegue el personal del Distrito Sanitario correspondiente o equipo de investigación a completar el estudio.</p>
<p class="boton_global">Ésta investigación incluye una revisión de la información de casos de ETA que hayan llegado al servicio de salud, para detectar las similitudes en relación a la fecha de inicio de los síntomas, número de enfermos, síntomas predominantes, alimentos sospechosos, lugar y hora cuando se consumió el alimento sospechoso (aquellos consumidos dentro de las 72 horas anteriores al inicio de los síntomas) y cualquier otra información de interés epidemiológico.</p>
<p class="boton_global">El personal del Distrito Sanitario correspondiente o equipo de investigación se desplazará a la mayor brevedad posible hacia los sitios donde se encuentran los comensales expuestos, (enfermos o no) y al local donde se preparó o consumió la comida sospechosa. La rapidez tiene como objetivo efectuar oportunamente las encuestas, la recolección de las muestras de alimentos sospechosos, muestras ambientales y muestras biológicas de las personas afectadas, antes que los pacientes reciban antibióticos y los alimentos sean eliminados.</p>
