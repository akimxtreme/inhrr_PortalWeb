// Funciones y Variables para Aplicar Aumentar, Disminuir y Normalizar el Tamaño de Letra del Portal Web
// INICIO
// Variables
var tgs = new Array('p','h1','a','h2','h3','li','div','span','ol','ul','strong','td','tr');
var szs = new Array('12px','13px','14px','14.5px','15px','15.5px','16px','16.5px');
var startSz = 1;
// Funciones
function ts( trgt,inc ) {
	if (!document.getElementById) return
	var d = document,cEl = null,sz = startSz,i,j,cTags;
	sz += inc;
	if ( sz < 2 ) sz = 1;
	if ( sz > 6 ) sz = 7;
	startSz = sz;
	if (!( cEl = d.getElementById( trgt ) ) ) cEl = d.getElementsByTagName( trgt )[ 0 ];
	cEl.style.fontSize = szs[ sz ];
	for ( i = 0 ; i < tgs.length ; i++ ) {
	cTags = cEl.getElementsByTagName( tgs[ i ] );
	for ( j = 0 ; j < cTags.length ; j++ ) cTags[ j ].style.fontSize = szs[ sz ];
	}
}
