function ciudadano(){
	var validado = true;
	
	//////////////////////////////////////////////////
	function id(id){								//	
		var elemento = document.getElementById(id);	//
		return elemento;							//
		}											//	
	//////////////////////////////////////////////////
	
	//////////////////////////////////////////////////
	// CAMPOS OBLIGATORIOS							//
	var campos = [									//
				id('nombre_ac'),					//
				id('apellido_ac'),					//
				id('correo_ac'),					//
				id('telefono_ac'),					//
				id('tema_ac'),						//
				id('titulo_ac'),					//
				id('mensaje_ac'),					//
				id('boton_ac')						//
				 ];									//
	//////////////////////////////////////////////////
	
	//////////////////////////////////////////////////
	for(i in campos){
		
		//////////////////////////////////////////////////////////////////////
		if((campos[i].value=="") || (campos[i].value=="Seleccione...") ){	//	
			incorrecto(campos[i]);											//
			validado = false;												//
			break;															//
			}else{															//
				correcto(campos[i]);										//
				}															//
		//////////////////////////////////////////////////////////////////////
		
		} // CIERRE for(i in campos)
		
	//////////////////////////////////////////////////
return validado;
	
}// ciudadano(CIERRE)

