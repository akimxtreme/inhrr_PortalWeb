	<h2 class="contenedor_1">Contraloría Sanitaria participa en el Operativo Carnaval 2013 </h2>
    <div class="sections70">
        <p class="columns">Caracas, 05.02.13 (Prensa MPPS).- Como antesala a las fiestas carnestolendas, el Servicio Autónomo de Contraloría Sanitaria (SACS) del Ministerio del Poder Popular para la Salud (MPPS) realizó este martes unas visitas de inspección a los establecimientos que expenden alimentos y bebidas en el Terminal La Bandera, para dar inicio al operativo Carnaval 2013; así lo informó Divis Antúnez, director general del SACS.</p>
        <p class="columns">Destacó que el objetivo es verificar que los comercios posean sus permisos sanitarios vigentes y que las condiciones higiénicas sanitarias del lugar sean las más adecuadas para el expendio de alimentos y bebidas, dentro del terminal terrestre.</p>
        <p class="columns">Tanto el SACS como el Instituto para la Defensa de las Personas en el Acceso de los Bienes y Servicios (Indepabis) desplegaron acciones conjuntas en el marco de un operativo especial de inspección y fiscalización Carnavales 2013, a fin de brindar a los usuarios y usuarias que hacen vida en los terminales terrestres, aéreos y marítimos del todo el territorio nacional, las condiciones óptimas en materia de salubridad.</p>    
        <p class="columns">Antúnez informó que el operativo se realiza en los 27 terminales terrestres del país y con la participación de más de 500 funcionarios del SACS e Indepabis; en tal sentido exhortó a la población venezolana a tomar acciones para así erradicar la especulación y denunciar a través del 0212-408.04.74 de la Dirección de Higiene de los Alimentos de la SACS, aquellos comercios que incurran en ilícitos.</p>
        <p class="columns">Por su parte, Consuelo Cerrada, presidenta del Indepabis, señaló que como parte de esta jornada revisarán que los transportistas cumplan con lo establecido en la Gaceta Oficial Nº 39.941 del 11 de junio de 2012, la cual regula los precios del transporte terrestre, para así evitar sobreprecio en la boletería </p>
