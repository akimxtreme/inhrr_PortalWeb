<div class="contenido_global" id="2">
<h3 class="contenedor_1">Red Interamericana de Laboratorios de Análisis de Alimentos (RILAA)</h3>
<p class="boton_global">Organización que desde Diciembre de 1997 viene apoyando a los laboratorios oficiales u oficialmente reconocidos de la región.</p>
<p class="boton_global"><strong>Misión:</strong> Promover la garantía de inocuidad y calidad de los alimentos en la región de las Américas para prevenir las enfermedades transmitidas por los mismos; proteger la salud del consumidor y facilitar el comercio promoviendo y fortaleciendo el desarrollo e interacción de los laboratorios analíticos dentro del marco de programas nacionales integrados de protección de los alimentos.</p>
<p class="boton_global"><strong>Objetivo General:</strong></p>
<ol class="letra">
<li class="letra">Lograr la equivalencia metodológica de los laboratorios de análisis de alimentos.</li>
<li class="letra">Promover la implementación de sistemas de gestión de la calidad equivalentes en los laboratorios de la RILAA.</li>
<li class="letra">Fortalecer la cooperación técnico-científica entre los países involucrados en la misma.</li>
</ol>
<p class="boton_global"><strong>Objetivos Específicos:</strong></p>
<ol class="letra">
<li class="letra">Desarrollar un sistema de información entre los laboratorios de la RILAA.</li>
<li class="letra">Facilitar la disponibilidad de los materiales de referencia y la participación en pruebas interlaboratorios.</li>
<li class="letra">Organizar y promover programas de capacitación y educación continua, fomentando el intercambio de experiencias y recursos disponibles en la región.</li>
<li class="letra">Promover y fortalecer la participación intersectorial, en la conformación y funcionamiento de las redes nacionales de laboratorios de alimentos.</li>
<li class="letra">Promover y fortalecer la integración de los laboratorios de la RILAA a los programas de protección de alimentos y vigilancia epidemiológica. Fuente: <a href="http://www.panalimentos.org/rilaa/" title="http://www.panalimentos.org/rilaa/" target="_blank"><strong>http://www.panalimentos.org/rilaa/</strong></a></li>
</ol>
</div>
