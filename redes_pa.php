<h3 class="contenedor_1">Red Internacional de Sub-tipificación Molecular para la Vigilancia de Enfermedades Transmitidas por Alimentos - (PULSENET)</h3>
<p class="boton_global">Red Internacional de Sub-tipificación Molecular para la Vigilancia de Enfermedades Transmitidas por Alimentos, desarrollada por los Centros para el Control de las Enfermedades (CDC) de USA, donde se utiliza el método de Electroforesis de Campo Pulsado (PFGE) para apoyar la Vigilancia de los Patógenos de Transmisión Alimentaria.</p>
<p class="boton_global"><strong>Objetivo:</strong> Reducir las Enfermedades de Transmisión Alimentaria promoviendo la utilización de Técnicas Moleculares para el diagnóstico y tipificación de patógenos a fin de fortalecer la vigilancia y el sistema de respuesta.</p>
<p class="boton_global"><strong>Objetivos Específicos: </strong></p>
<ol class="letra">
<li class="letra">Reforzar la Vigilancia Nacional y Regional de las ETA.</li>
<li class="letra">Mejorar el reconocimiento, diagnóstico e investigación de brotes.</li>
<li class="letra">Detectar tempranamente patógenos emergentes y re-emergentes.</li>
<li class="letra">Establecer bases de datos Nacional y Regional.</li>
<li class="letra">Fortalecer la comunicación entre países.</li>
<li class="letra">Consolidar la colaboración entre laboratorios de alimentos, laboratorios de diagnóstico clínico y epidemiología.</li>
</ol>
<p class="boton_global">PulseNet América Latina inicia sus actividades en 2004, en la actualidad se denomina PulseNet América Latina y Caribe, bajo la Coordinación del Dr. Enrique Pérez y la Dra. Mariana Pichel, cuenta con la participación de: Argentina, Bolivia, Brasil, Región Carec, Chile, Colombia, Costa Rica, Cuba, Ecuador, Uruguay y Venezuela.</p>
<p class="boton_global">El Instituto Nacional de Higiene “Rafael Rangel” ha representado al País en las reuniones de PulseNet. Fuente nº1: <a href="http://www.panalimentos.org/pulsenet/" title="http://www.panalimentos.org/pulsenet/" target="_blank"><strong>http://www.panalimentos.org/pulsenet/</strong></a>; Fuente nº2: <a href="http://www.cdc.gov/pulsenet/" title="http://www.cdc.gov/pulsenet/
