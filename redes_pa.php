<h3 class="contenedor_1">Red Venezolana de Laboratorios de Análisis de Alimentos (REVLAA)</h3>
<p class="boton_global">El Instituto Nacional de Higiene “Rafael Rangel” en su carácter de Laboratorio de Referencia del Ministerio del Poder Popular para la Salud y representante de Venezuela en la RILAA, con auspicio de OPS/Venezuela eI NPPAZ Argentina, planificó el primer taller para la conformación de la Red Venezolana de Laboratorios de Análisis de Alimentos (REVLAA), la cual tuvo lugar el día 15 de Octubre de 1.999 en la sede de éste Instituto, con la participación de representantes de la Dirección de Higiene de los Alimentos, OPS/Venezuela, INPPAZ OPS/OMS, laboratorios del Ministerio de Salud y Desarrollo Social, Fundación CIEPE, Fundación La Salle, Universidades Nacionales, Fonaiap Sucre y el Instituto Nacional de Higiene “Rafael Rangel”.</p>
<p class="boton_global">De ésta iniciativa surgió la Red Venezolana de Laboratorios de Análisis de Alimentos (REVLAA), cuyos objetivos son:</p>
<ol class="letra">
<li class="letra">Integrar y fortalecer los recursos técnicos – científicos disponibles en el país.</li>
<li class="letra">Incrementar y promover la cooperación analítica entre los laboratorios miembros.</li>
<li class="letra">Garantizar el control de la calidad e inocuidad de los alimentos de consumo humano.</li>
<li class="letra">Permitir generar respuestas oportunas que faciliten y/o aceleren la toma de decisiones que garanticen en pro de la salud pública.</li>
</ol>
<p class="boton_global">En el año 2009, se reactivan las actividades de la REVLAA y se establece la Normativa de funcionamiento mediante el documento <a href="pdf/rc_pdf/pdf_pa/2012/N-PERC-002.pdf" title="Normativa de la Red Venezolana de Análisis de Alimento- REVLAA" target="_blank"><strong>N-PERC-002 “Normativa de la Red Venezolana de Análisis de Alimento- REVLAA”</strong></a>, aprobado por el Consejo del Instituto Nacional de Higiene “Rafael Rangel”.</p>

