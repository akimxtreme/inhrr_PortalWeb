<h1 class="contenedor_0">ANTECEDENTES</h1>

<p class="columns2">En Venezuela desde el año 1883, la fiscalización estatal de los medicamentos ha sido una actividad sanitaria establecida en el Reglamento del “Consejo de Médicos”, que expresa: ninguna medicina secreta o patentada podía ser expedida sin haber sido aprobada por dicho organismo. Desde aquella época, medicamentos de dudosa o escasa utilidad terapéutica que presentaran efectos indeseables y/o tóxicos, no eran aceptados para el consumo humano. Desde ese entonces a la actualidad la vigilancia de los medicamentos ha sufrido cambios importantes en el país, para garantizarnos la seguridad; calidad y eficacia de los mismos.</p>
<p class="columns2">Para el año 1987, se crea el Centro Nacional de Vigilancia Farmacológica (CENAVIF) en el Instituto Nacional de Higiene “Rafael Rangel” como unidad de la Gerencia Sectorial de Registro y Control, siendo oficializado dicho centro en el año 1989, ante la necesidad de vigilar las reacciones adversas a medicamentos así como de comprobar la eficacia de los mismos y establecer el balance beneficio - riesgo del medicamento durante su fase de comercialización.</p>
<p class="columns">Desde el año 1995 el CENAVIF forma parte del Programa Internacional de Monitoreo Mundial de Reacciones Adversas, del Centro de Monitoreo de Uppsala en Suecia; el cual es el Centro Colaborador de la Organización Mundial de la Salud (UMC-OMS), donde se debe reportar cuatrimestralmente las Reacciones Adversas de Medicamentos (RAM) que ocurren a nivel nacional y de donde se reciben alertas internacionales de medicamentos.</p>
<p class="columns2">Actualmente el CENAVIF es el Centro nacional de referencia y organismo integrador de los planes y programas inherentes a la farmacovigilancia; el cual recibe, procesa, evalúa y recomienda las medidas regulatorias en esta materia para el Ministerio del Poder Popular para la Salud (MPPS) a través de la Gerencia Sectorial de Registro y Control y la Junta Revisora de Productos Farmacéuticos.</p>
<p class="columns2">En marzo de 2009, se crea en el CENAVIF, la Coordinación Farmacéutica, a fin de completar las actividades de farmacovigilancia, con la detección de fallas y desvíos de calidad en medicamentos, con el objetivo de reducir el riesgo que involucra la utilización de medicamentos de calidad subestándar, degradados o falsificados.</p>
<p class="columns">Aún los retos mundiales y nacionales en este tema de vital importancia sanitaria son inmensos; más cuando  se conoce que los problemas asociados a uso indebido de medicamentos , a problemas sanitarios producto de la desviación de calidad, a los efectos adversos ahora mirados sobre la población general “o el colectivo” que reflejan una verdad mas cercana, de seguridad del medicamento más cuando sabemos que ningún medicamento es 100% seguro.</p>
<p class="columns">Que la falsificación de medicamentos es una realidad preocupante.</p>
<p class="columns">Que la automedicación se ha expandido de manera alarmante.</p>
<p class="columns">Por ello nos esforzamos en mejorar nuestro accionar en este tema y requerimos de la participación decidida de los profesionales de salud obligados éticamente a reportarlos RAMs y de la industria obligada a hacerlo por el cumplimiento de normas regulatorias y al colectivo(usuario de medicamento),por ser el afectado. Ayudarnos será siempre agradecido.</p>
