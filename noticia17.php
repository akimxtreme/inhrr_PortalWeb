        <h3 class="contenedor_1">La Hipertensión</h3>
        <p class="columns"><strong>La hipertensión</strong> – también conocida como tensión arterial alta – aumenta el riesgo de infartos de miocardio, accidentes cerebrovasculares e insuficiencia renal.</p>
        <p class="columns">La hipertensión no controlada puede causar también ceguera, irregularidades del ritmo cardiaco y fallo cardiaco.</p>
        <p class="columns">El riesgo de que se presenten esas complicaciones es mayor si se dan otros factores de riesgo cardiovascular como la diabetes.</p>
        <p class="columns">Uno de cada tres adultos del mundo padece hipertensión. Esa proporción aumenta con la edad: una de cada diez personas de 20 a 40 años, y cinco de cada diez de 50 a 60 años.</p>
        <p class="columns">La prevalencia más alta se observa en algunos países de ingresos bajos de África; se cree que la hipertensión afecta a más del 40% de los adultos en muchos países de ese continente.</p>
        <p class="columns">No obstante, la hipertensión se puede prevenir y tratar. En algunos países en desarrollo, la prevención y el tratamiento de la hipertensión, y de otros factores de riesgo cardiovascular, ha hecho que disminuyan las muertes por cardiopatías.</p>
