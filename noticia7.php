    <p class="columns">El Instituto Nacional de Higiene Rafael Rangel (INH ”RR”) organiza y desarrolla con apoyo del Ministerio del Poder Popular para la Salud (MPPS) un programa de control de calidad con el propósito de evaluar el desempeño de los laboratorios de banco de sangre a nivel nacional, públicos y privados, abarcando las etapas pre-analítica, analítica y post-analítica bajo una total confidencialidad. Este programa lleva por nombre <strong>PROGRAMA DE CONTROL DE CALIDAD EXTERNO EN SEROLOGÍA (PCCES)</strong>, el cual es responsabilidad de la Gerencia Registro y Control desde el año 2007.</p>
	<p class="columns">Es un programa establecido con la finalidad de dar garantía de calidad a los tamizajes serológicos y asegurar la calidad de las transfusiones realizadas, específicamente con respecto a los parámetros: Virus de Inmunodeficiencia Humana, Virus Linfotrópico de células T Humanas, Hepatitis B y C, Sífilis y Chagas.</p>
	<p class="columns">Para ello, el laboratorio organizador distribuye el trabajo a realizar en varias etapas, como son: la recolección de la materia prima, la producción de paneles de suero, la caracterización de los sueros producidos, el análisis de los resultados obtenidos, la selección de las muestras que formarán parte del programa en base a los criterios de inclusión y exclusión, el envío de los paneles, la recepción de los resultados emitidos por los laboratorios participantes, la liberación de las claves para permitir la autoevaluación de cada laboratorio participante y la emisión de un certificado o una constancia de participación según corresponda.</p>
	<p class="columns">El PCCES permite que los bancos de sangre participantes valoren sus propios procedimientos internos de control de calidad y pongan en práctica los ajustes eventualmente necesarios a fin de mejorar sus procesos, implementando las medidas correctivas y/o preventivas requeridas. De igual manera aporta información al MPPS acerca del desempeño y necesidades de los laboratorios participantes, para el diseño de políticas de mejora en el ámbito de salud pública.</p>
