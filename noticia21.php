	<h2 class="contenedor_1">Venezuela brinda alternativa gratuita en la región para tratamiento por cáncer</h1>
    <div class="sections3">
    	<p class="columns">Autoridades del Instituto Venezolano de los Seguros Sociales (IVSS) y del Ministerio para la Salud inspeccionaron este martes los avances y proyecciones del complejo hematoncológico y de radiocirugía más grande del país como una de las mejores alternativas gratuitas en el continente para los pacientes con cáncer.</p>
        <p class="columns">Desde las instalaciones del hospital Domingo Luciani, en El Llanito, Caracas, el recorrido fue dirigido por el presidente del IVSS, Carlos Rotondaro, quien explicó que es el único centro en el continente que brinda sin costo alguno para el paciente un sistema de calidad para aplicar tratamientos de quimioterapia, radioterapia y radiocirugía con tecnología de última generación.</p>
        <p class="columns">
    El complejo cuenta con el robot Cyberknife, diseñado para ejecutar radiocirugías no invasivas de alta precisión, a través de un sistema computarizado. La radiocirugía con Cyberknife es practicada en Estados Unidos, México, Colombia y Brasil, pero en Venezuela es el único país del mundo con este servicio puesto a la orden de los ciudadanos sin costo alguno. Se espera instalar un segundo equipo en el Hospital Padre Machado.
   		</p>
        <p class="columns">
  “Hay casos y tratamientos cuyos montos oscilan alrededor de los 130 mil bolívares en el sector privado que se están aplicando de forma gratuita a los que acudan al centro, sin importar su afiliación a algún seguro, solo con la solicitud del médico tratante o la referencia de otro centro de salud”, explicó Rotondaro.
    	</p>
