<h3 class="contenedor_1">Trámites para Evaluación Integral</h3>
<p class="boton_global">La Evaluación Integral de  Repelentes de Insectos de Uso Tópico en el Instituto Nacional de Higiene “Rafael Rangel” comprende el estudio documental  del expediente y/o el análisis de las muestras, necesarios para asegurar la eficacia, seguridad y calidad de estos  productos comercializados en la República Bolivariana de Venezuela. La misma se puede realizar con anterioridad a la obtención del Registro Sanitario o posterior al mismo.</p>
<p class="boton_global">Los requisitos dependerán del tipo de trámite a realizar. La primera vez que un Farmacéutico Patrocinante acude a nuestras instalaciones a tramitar una solicitud para un Repelente de Insectos de Uso Tópico, adicionalmente a los recaudos solicitados para el trámite, deberá consignar, por una única vez, original y copia de los siguientes documentos:</p>

<ol class="numero">
	<li class="numero">Poder que lo acredita como farmacéutico Patrocinante de la Empresa solicitante del trámite.</li>
    <li class="numero">Rif actualizado de la Empresa.</li>
    <li class="numero">Cedula de identidad del Farmacéutico Patrocinante.</li>
</ol>
</div>

<div class="contenido_global" id="2">
<h3 class="contenedor_1">Registro Sanitario</h3>
<ol class="numero">
<li class="numero">Cancele en el banco la tarifa correspondiente a “Evaluación Integral de Repelentes de Insectos de Uso Tópico” establecida en la Gaceta Oficial  Nº   39.819  de fecha 13 de Diciembre de 2011.</li>
<li class="numero">Solicite una cita  a través de la página Web: <a href="http://www.inhrr.gob.ve/" title="Portal Oficial del Instituo Nacional de Higiene 'Rafael Rangel'" target="_blank"><strong>www.inhrr.gob.ve.</strong></a></li>
<li class="numero">Descargue y complete el Formulario  <a href="pdf/rc_pdf/pdf_pc/2012/instructivo_solicitud_repelentes.pdf" title="Solicitud de Registro Sanitario de Repelentes de Insectos de Uso Tópico" target="_blank"><strong>“Solicitud de Registro Sanitario de Repelentes de Insectos de Uso Tópico”.</strong></a></li>
<li class="numero">Coloque en el formulario Timbre fiscal por un monto de 0,02 U.T. e inutilizarlo.</li>
<li class="numero">Descargue y complete la hoja de Control de Pago: <a href="doc/rc_doc/rc_pc/Formulario_Ctrol_Pago_PC.doc" title="" target="_blank"><strong>Formulario Control de Pago para Solicitudes de Productos Cosméticos.</strong></a> </li>
<li class="numero">Prepare en una carpeta marrón, tamaño oficio, debidamente identificada con el nombre del producto, los siguientes recaudos:
	<ul class="vinetas">
    	<li class="vinetas">Formulario con los datos solicitados.</li>
        <li class="vinetas">Timbre fiscal de 0.02 U.T.</li>
        <li class="vinetas">Fórmula cuali-cuantitativa.</li>
        <li class="vinetas">Justificación de ingredientes.</li>
        <li class="vinetas">Especificaciones organolépticas y fisicoquímicas del producto terminado.</li>
        <li class="vinetas">Especificaciones microbiológicas cuando corresponda, de acuerdo a la naturaleza del producto terminado.</li>
        <li class="vinetas">Protocolo de análisis físico – químico de los principios activos y de todas las sustancias con limitaciones de uso.</li>
        <li class="vinetas">Métodos de análisis de las sustancias y/o principios activos con limitaciones.</li>
        <li class="vinetas">Patrones de referencia de los principios activos con su certificado de análisis y especificaciones físico – químicas.</li>
        <li class="vinetas">Análisis toxicológico en el producto terminado que demuestren su seguridad de uso y tolerabilidad.</li>
        <li class="vinetas">Texto de etiqueta del envase, estuche y folleto, cuando corresponda.</li>
        <li class="vinetas">Instrucciones de uso del producto, cuando corresponda.</li>
        <li class="vinetas">Material del envase primario.</li>
    </ul>
</li>
<li class="numero">Consigne el día y la hora de la cita otorgada ante el Instituto Nacional de Higiene “Rafael Rangel”, Sección de Recepción de Cosméticos, la carpeta preparada según el punto Nº 7, y los siguientes recaudos:
	<ul class="vinetas">
    	<li class="vinetas">Constancia de la cita.</li>
        <li class="vinetas">Depósito Bancario.</li>
        <li class="vinetas">Hoja de Control de Pago (original y copia).</li>
    </ul>
</li>
<li class="numero"><strong>Admisión:</strong> Para ser admitida a trámite una solicitud de un Producto Cosmético en el Instituto Nacional de Higiene “Rafael Rangel” debe seguir el procedimiento siguiente:
	<ul class="vinetas">
    	<li class="vinetas">El Farmacéutico Patrocinante Presenta los recaudos solicitados de acuerdo al trámite a realizar.</li>
        <li class="vinetas">El Farmacéutico Receptor revisa los recaudos presentados.</li>
        <li class="vinetas">Si los recaudos  consignados presentan alguna inconformidad, se devolverá el trámite, indicando  (por escrito) al Farmacéutico Patrocinante el motivo de la devolución, el cual deberá subsanar la inconformidad y reiniciar el proceso.</li>
        <li class="vinetas">Si los recaudos  consignados están conformes, el farmacéutico receptor le  asigna el número correspondiente según sea el trámite.</li>
        <li class="vinetas">El farmacéutico Patrocinante se dirige a la Caja para que le sea emitida la factura correspondiente y selladas las hojas de Control de Pago.</li>
        <li class="vinetas">El Farmacéutico Patrocinante consigna en la Recepción de Cosméticos una copia de la Hoja de Control de Pago sellada por caja.</li>
    </ul>
</li>
