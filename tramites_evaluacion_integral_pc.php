<li class="numero"><strong>Primera Cita:</strong> La primera vez que un Farmacéutico Patrocinante acude a nuestras instalaciones a tramitar una solicitud para un Producto Cosmético, adicionalmente a los recaudos solicitados para el trámite, deberá consignar, por una única vez, original y copia de los siguientes documentos:
	<ol class="numero">
    	<li class="numero">Poder que lo acredita como Farmacéutico Patrocinante de la Empresa solicitante del trámite.</li>
        <li class="numero">Rif actualizado de la Empresa.</li>
        <li class="numero">Cédula de identidad del Farmacéutico Patrocinante.</li>
    </ol>
</li>
<li class="numero"><strong>Notificación Sanitaria Obligatoria ó Reconocimiento Mutuo:</strong> Pasos a Seguir:
	<ol class="numero">
    	<li class="numero">Cancele en el banco la tarifa establecida para “Evaluación Integral de Productos Cosméticos” en la Gaceta Oficial  Nº   39.819  de fecha 13 de Diciembre de 2011.</li>
        <li class="numero">Solicite una cita  a través de la página Web: <a href="http://www.inhrr.gob.ve" title="Portal Oficial de Instituto Nacional de Higiene 'Rafael Rangel'"><strong>www.inhrr.gob.ve</strong></a></li>
        <li class="numero">Descargue  y complete el Formulario  <a href="doc/rc_doc/rc_pc/Formulario_Solicitud_PC.doc" title="Formulario de Solicitud de Notificación Sanitaria Obligatoria, Renovación o  Reconocimiento Mutuo de Productos Cosméticos'" target="_blank"><strong>“Solicitud de Notificación Sanitaria Obligatoria, Renovación o  Reconocimiento Mutuo de Productos Cosméticos”</strong></a></li>
        <li class="numero">Coloque en el formulario Timbre fiscal por un monto de 0,02 U.T. e inutilizarlo.</li>
        <li class="numero">Descargue y complete la hoja de Control de Pago: <a href="doc/rc_doc/rc_pc/Formulario_Ctrol_Pago_PC.doc" title="" target="_blank"><strong>Formulario Control de Pago para Solicitudes de Productos Cosméticos.</strong></a> </li>
        <li class="numero">Prepare en una carpeta marrón, tamaño oficio, debidamente identificada con el nombre del producto, los siguientes recaudos: 
        	<ul class="vinetas">
            	<li class="vinetas">Formulario de “Solicitud de Notificación Sanitaria Obligatoria, Renovación o  Reconocimiento Mutuo de Productos Cosméticos” con su respectivo timbre fiscal. </li>
                <li class="vinetas">Cuando se trate de Reconocimiento Mutuo el interesado deberá anexar una copia del Certificado de Notificación Sanitaria Obligatoria del País donde fue notificado.</li>
           	</ul>
        </li>
        <li class="numero">Consigne el día y la hora señalada, ante el Instituto Nacional de Higiene “Rafael Rangel” (INH”RR”), Sección de Recepción Cosméticos, la carpeta preparada según el punto Nº 6, y los siguientes recaudos:
        	<ul class="vinetas">
            	<li class="vinetas">Constancia de la cita.</li>
                <li class="vinetas">Depósito Bancario.</li>
                <li class="vinetas">Hoja de Control de Pago (original y copia).</li>
           	</ul>
        </li>
        <li class="numero"><strong>Admisión:</strong> Para ser admitida a trámite una solicitud de un Producto Cosmético en el Instituto Nacional de Higiene “Rafael Rangel” debe seguir el procedimiento siguiente:
        	<ul class="vinetas">
            	<li class="vinetas">El Farmacéutico Patrocinante Presenta los recaudos solicitados de acuerdo al trámite a realizar.</li>
                <li class="vinetas">El Farmacéutico Receptor revisa los recaudos presentados.</li>
                <li class="vinetas">Si los recaudos  consignados presentan alguna inconformidad, se devolverá el trámite, indicando  (por escrito) al Farmacéutico Patrocinante el motivo de la devolución, el cual deberá subsanar la inconformidad y reiniciar el proceso.</li>
