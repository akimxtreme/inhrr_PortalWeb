<h1 class="contenedor_0">TRÁMITES DE EVALUACIÓN INTEGRAL CON FINES DE REGISTRO SANITARIO</h1>

<p class="boton_global">La Evaluación Integral de los Productos Cosméticos comprende el estudio documental del expediente y/o el análisis de las muestras, necesario para asegurar la eficacia, seguridad y calidad de los Productos Cosméticos comercializados en la República Bolivariana de Venezuela. La misma se puede realizar con anterioridad a la obtención de la Notificación Sanitaria Obligatoria o posterior a la misma.</p>
<p class="boton_global">Los requisitos dependerán del tipo de trámite a realizar:</p>

